import math

def dsin(x):
    r = math.radians(x)
    return math.sin(r)

def dcos(x):
    r = math.radians(x)
    return math.cos(r)

def dtan(x):
    r = math.radians(x)
    return math.tan(r)

def dsinh(x):
    r = math.radians(x)
    return math.sinh(r)

def dcosh(x):
    r = math.radians(x)
    return math.cosh(r)

def dtanh(x):
    r = math.radians(x)
    return math.tanh(r)

def dasin(x):
    r = math.radians(x)
    return math.asin(r)

def dacos(x):
    r = math.radians(x)
    return math.acos(r)

